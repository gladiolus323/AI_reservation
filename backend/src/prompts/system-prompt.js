// hospital_rules.txt 로드
function loadHospitalRules() {
    try {
        const rulesPath = path.join(__dirname, '../../../data/hospital_rules.txt');
        return fs.readFileSync(rulesPath, 'utf-8');
    } catch (error) {
        return '';
    }
}

